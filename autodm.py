def get_monster(monsterCR, monsterEnv):
    # No CR
    if monsterCR == "None":
        monsters = [monster["Name"] for monster
                    in allmonsters
                    if monsterEnv in monster['Env']]
    # No Env
    elif monsterEnv == "None":
        monsters = [monster["Name"] for monster
                    in allmonsters
                    if monster['CR'] == monsterCR]       
    # Both CR and Env
    else:
        monsters = [monster["Name"] for monster
                    in allmonsters
                    if monsterEnv in monster['Env']
                    and monster['CR'] == monsterCR]

    # Get random monster
    if len(monsters):
        monster = random.choice(monsters)
    else:
        monster = "None"

    # Return
    return monster


